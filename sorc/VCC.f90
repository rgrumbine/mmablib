      SUBROUTINE VCC(X1,Y1,X2,Y2,NS,R2)
      IMPLICIT none
!
!   PROGRAM IN FORTRAN 77 TO CALCULATE THE VECTOR CORRELATION
!   COEFFICIENT OF JUPP AND MARDIA(BIOMETRIKA,1980).
!
!   THE FOLLOWING PROGRAM CALCULATES THE VECTOR CORRELATION BETWEEN
!   SEQUENCES OF TWO-DIMENSIONAL VECTORS. THESE VECTORS MUST BE
!   SPECIFIED IN TERMS OF THEIR U(X1,Y1) AND V(X2,Y2) COMPONENTS.
!   FOR TWO-DIMENSIONAL VECTORS, THE VECTOR CORRELATION COEFFICIENT
!   (SQUARED) WILL VARY BETWEEN 0.0(UNCORRELATED) AND 2.0
!   (COMPLETELY CORRELATED).
!
!   VECTOR CORRELATION COEFFICIENTS CALCULATED ACCORDING TO THE
!   FOLLOWING PROGRAM SHOULD BE ACCURATE TO AT LEAST THREE DECIMAL
!   PLACES USING SINGLE PRECISION ARITHMETIC.
!
!   NS IS THE SAMPLE SIZE - SAMPLE SIZES LESS THAN 8 ARE NOT
!   RECOMMENDED BECAUSE THE ASYMPTOTIC CHI-SQUARE DISTRIBUTION
!   BREAKS DOWN FOR SMALLER VALUES.
!
! Origin: Crosby, Breaker, Gemmill
!
! Maintenance: Robert Grumbine
! Dec 1998
!
      INTEGER NS
      REAL VAR1,VAR2,VAR3,VAR4,COV12,COV13,COV14,COV23,COV24, &
        COV34,X1(NS),Y1(NS),X2(NS),Y2(NS),R2
!
        CALL STAT1(X1,Y1,X2,Y2,NS,VAR1,VAR2,COV12)
        CALL STAT2(X1,Y1,X2,Y2,NS,VAR1,VAR3,COV13)
        CALL STAT3(X1,Y1,X2,Y2,NS,VAR1,VAR4,COV14)
        CALL STAT4(X1,Y1,X2,Y2,NS,VAR2,VAR3,COV23)
        CALL STAT5(X1,Y1,X2,Y2,NS,VAR2,VAR4,COV24)
        CALL STAT6(X1,Y1,X2,Y2,NS,VAR3,VAR4,COV34)
        CALL RSQ(VAR1,VAR2,VAR3,VAR4,COV12,COV13,COV14,COV23, &
        COV24,COV34,R2)
      RETURN
      END
      SUBROUTINE STAT1(X1,Y1,X2,Y2,NS,VAR1,VAR2,COV12)
      IMPLICIT none
      INTEGER I, NS
      REAL VAR1,VAR2,COV12,SUMX1,SUMY1,SX1SQ,SY1SQ,SX1Y1, &
        X1(*),Y1(*),X2(*),Y2(*)
      SUMX1 = 0.0
      SUMY1 = 0.0
      SX1SQ = 0.0
      SY1SQ = 0.0
      SX1Y1 = 0.0
      DO I = 1,NS
        SUMX1 = SUMX1 + X1(I)
        SUMY1 = SUMY1 + Y1(I)
        SX1SQ = SX1SQ + X1(I)*X1(I)
        SY1SQ = SY1SQ + Y1(I)*Y1(I)
        SX1Y1 = SX1Y1 + X1(I)*Y1(I)
      ENDDO
      VAR1 = (FLOAT(NS) * SX1SQ - SUMX1 * SUMX1) / FLOAT(NS * (NS - 1))
      VAR2 = (FLOAT(NS) * SY1SQ - SUMY1 * SUMY1) / FLOAT(NS * (NS - 1))
      COV12 = (FLOAT(NS) * SX1Y1 - SUMX1 * SUMY1) / FLOAT(NS * (NS - 1))
      RETURN
      END
      SUBROUTINE STAT2(X1,Y1,X2,Y2,NS,VAR1,VAR3,COV13)
      IMPLICIT none
      INTEGER I, NS
      REAL VAR1,VAR3,COV13,SUMX1,SUMX2,SX1SQ,SX2SQ,SX1X2, &
         X1(*),Y1(*),X2(*),Y2(*)
      SUMX1 = 0.0
      SUMX2 = 0.0
      SX1SQ = 0.0
      SX2SQ = 0.0
      SX1X2 = 0.0
      DO I = 1,NS
        SUMX1 = SUMX1 + X1(I)
        SUMX2 = SUMX2 + X2(I)
        SX1SQ = SX1SQ + X1(I)*X1(I)
        SX2SQ = SX2SQ + X2(I)*X2(I)
        SX1X2 = SX1X2 + X1(I)*X2(I)
      ENDDO
      VAR1 = (FLOAT(NS) * SX1SQ - SUMX1 * SUMX1) / FLOAT(NS * (NS - 1))
      VAR3 = (FLOAT(NS) * SX2SQ - SUMX2 * SUMX2) / FLOAT(NS * (NS - 1))
      COV13 = (FLOAT(NS) * SX1X2 - SUMX1 * SUMX2) / FLOAT(NS * (NS - 1))
      RETURN
      END
      SUBROUTINE STAT3(X1,Y1,X2,Y2,NS,VAR1,VAR4,COV14)
      IMPLICIT none
      INTEGER I, NS
      REAL VAR1,VAR4,COV14,SUMX1,SUMY2,SX1SQ,SY2SQ,SX1Y2, &
